      <p>When adults shut down dark, scary, or aggressive themes in pretend play — the bear getting hurt, the bear being mean, the bear destroying the house — those feelings don't disappear. They go underground. We sometimes call this the hot lava problem: the unexpressed rage and grief and fear bubble beneath the surface until they erupt as real behavior, against real children, often without warning.</p>
      
